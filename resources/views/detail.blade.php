                        <h2>1. จัดตารางทบทวนแบบ "สรุป" ไม่ใช่ "เริ่มต้น"</h2>
                        <p>ช่วงสุดท้ายไม่ใช่เวลามาเริ่มอ่านเนื้อหาใหม่ทั้งหมด แต่เป็นการทบทวนสิ่งที่เรียนมาแล้ว ลองใช้เทคนิค "Active Recall" คือการพยายามนึกถึงเนื้อหาโดยไม่ดูหนังสือ แล้วค่อยเปิดเช็คความถูกต้อง วิธีนี้จะช่วยให้จำได้แม่นยำกว่าการอ่านซ้ำไปเรื่อยๆ</p>
                        <ul>
                            <li><strong>จัดลำดับความสำคัญ:</strong> ทบทวนบทที่ไม่ถนัดก่อน แล้วค่อยไปบทที่มั่นใจแล้ว</li>
                            <li><strong>ใช้ Flashcards:</strong> ทำการ์ดคำศัพท์หรือสูตรสั้นๆ เพื่อทบทวนได้ทุกที่ทุกเวลา</li>
                            <li><strong>สรุปเป็น Mind Map:</strong> การวาดแผนผังความคิดจะช่วยให้เห็นภาพรวมของเนื้อหาและเชื่อมโยงความสัมพันธ์ได้ดีขึ้น</li>
                        </ul>
                        <h2>2. ฝึกทำข้อสอบเก่าและจับเวลา</h2>
                        <p>การทำความคุ้นเคยกับรูปแบบข้อสอบและการบริหารเวลาเป็นหัวใจสำคัญ ควรหาข้อสอบเก่าหรือชุดข้อสอบจำลองมาทำโดยจับเวลาเหมือนสอบจริง เพื่อให้นักเรียนได้ฝึกการตัดสินใจว่าจะทำข้อไหนก่อน และจะข้ามข้อที่ทำไม่ได้ไปตอนไหน</p>
                        <h2>3. วิเคราะห์จุดพลาดและแก้ไข</h2>
                        <p>หลังจากทำข้อสอบแต่ละชุดแล้ว อย่าเพิ่งรีบไปทำชุดใหม่ ให้ใช้เวลาวิเคราะห์ข้อที่ทำผิดก่อน ว่าผิดเพราะอะไร? ไม่เข้าใจเนื้อหา, คำนวณผิด, หรืออ่านโจทย์ไม่ละเอียด? การเข้าใจจุดอ่อนของตัวเองจะทำให้การทบทวนครั้งต่อไปตรงจุดมากขึ้น</p>
                        <h2>4. ดูแลสุขภาพกายและใจให้พร้อม</h2>
                        <p>สมองจะทำงานได้ดีที่สุดเมื่อร่างกายแข็งแรงและจิตใจผ่อนคลาย</p>
                        <ul>
                            <li><strong>นอนให้พอ:</strong> ควรนอนหลับอย่างน้อย 8 ชั่วโมงต่อคืน โดยเฉพาะคืนก่อนสอบ</li>
                            <li><strong>กินอาหารที่มีประโยชน์:</strong> เน้นผัก ผลไม้ และโปรตีน หลีกเลี่ยงของหวานหรืออาหารขยะที่ทำให้ง่วงซึม</li>
                            <li><strong>หาเวลาพักผ่อน:</strong> ทำกิจกรรมที่ชอบสั้นๆ เพื่อคลายเครียด เช่น ฟังเพลง, วาดรูป, หรือออกกำลังกายเบาๆ</li>
                        </ul>
